import pygame


def draw_bitmap(surface, color, background, bitmap, width, height, x, y, pixel_size: int = (1, 1)):
    pixel_x = x
    pixel_y = y
    for index_x in range(x, x + width):
        for index_y in range(y, y + height):
            index = (index_y - y) * width + index_x - x
            if bitmap[index]:
                pygame.draw.rect(surface, color, (pixel_x, pixel_y, *pixel_size))
            else:
                pygame.draw.rect(surface, background, (pixel_x, pixel_y, *pixel_size))

            pixel_y += pixel_size[1]
        pixel_x += pixel_size[0]
        pixel_y = y


def draw_text(surface, text, color, background, x, y, pixel_size=(5, 5)):
    largest_x = 0

    x_offset = 0
    y_offset = 0
    for letter in text:

        if letter == "\n":
            if largest_x < x_offset:
                largest_x = x_offset
            x_offset = 0
            y_offset += FONT_SIZE[1] * pixel_size[1]
            continue

        if bitmap := BITMAP_LETTERS_FONT.get(letter):
            draw_bitmap(
                surface,
                color, background,
                bitmap, *FONT_SIZE,
                x + x_offset,
                y + y_offset,
                pixel_size=pixel_size
            )
        x_offset += FONT_SIZE[0] * pixel_size[0]
    
    if largest_x < x_offset:
        largest_x = x_offset
    return int(largest_x / pixel_size[0]), int(y_offset / pixel_size[1])


